#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	size_t	size;
	size_t	i;

	size = ft_strlen(s1) + 1;
	str = (char *)malloc(sizeof(char) * size);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
